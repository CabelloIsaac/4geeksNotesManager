public class CategoriasClass {

    String id, nombre;

    public CategoriasClass(String id, String nombre) {

        this.id = id;
        this.nombre = nombre;

    }

    public String getId() {
        return id;
    }

    public String getNombre() {
        return nombre;
    }

}